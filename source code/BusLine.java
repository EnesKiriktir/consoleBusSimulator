package proje2;
public class BusLine {

	int coor_x;
	int coor_y;

	public BusLine(int coor_x, int coor_y) 
	{
		this.coor_x = coor_x;
		this.coor_y = coor_y;
	}

	public int getCoor_x() 
	{
		return coor_x;
	}

	public void setCoor_x(int coor_x) 
	{
		this.coor_x = coor_x;
	}

	public int getCoor_y() 
	{
		return coor_y;
	}

	public void setCoor_y(int coor_y) 
	{
		this.coor_y = coor_y;
	}
}
